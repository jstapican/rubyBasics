# For Loops
friends = ["Tom", "Lance", "Eric", "Daryl"]

for friend in friends
  puts friend
end


puts
puts

# Shorthand Method - For Loops
friends = ["Tom", "Lance", "Eric", "Daryl"]

friends.each do |friend|
  puts friend
end


puts
puts

# Looping through Numbers - For Loops
for index in 0..5
  puts index
end


puts
puts

# Shorthand Looping through Numbers - For Loops
6.times do |index|
  puts index
end
